#pragma once

#include "DrawData.h"
#include "GameData.h"
#include "Debug.h"


class Manager
{
public:
	Manager() = default;
	~Manager() = default;

	////////////////////////////////////////////////////////////
	/// \brief Is called when the program initializes.
	////////////////////////////////////////////////////////////
	virtual void awake(GameData& _game_data) {};

	////////////////////////////////////////////////////////////
	/// \brief Called every cycle of the game loop.
	///	\param _timer DeltaTime.
	////////////////////////////////////////////////////////////
	virtual void update(GameData& _game_data) = 0;

	////////////////////////////////////////////////////////////
	/// \brief Called every cycle of the game loop, but after update.
	///	\param _timer DeltaTime.
	////////////////////////////////////////////////////////////
	virtual void lateUpdate(GameData& _game_data) {};

	////////////////////////////////////////////////////////////
	/// \brief Called every cycle of the game loop, but after update.
	///	\param _draw_data DrawData pointer.
	////////////////////////////////////////////////////////////
	virtual void render(DrawData* _draw_data) {};

	////////////////////////////////////////////////////////////
	/// \brief Called every cycle of the game loop, but after update.
	///	\param _draw_data DrawData pointer.
	////////////////////////////////////////////////////////////
	virtual void render2D(DrawData2D* _draw_data) {};
};
